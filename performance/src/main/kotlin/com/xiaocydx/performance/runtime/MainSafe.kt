package com.xiaocydx.performance.runtime

import android.os.Looper

internal val isMainThread: Boolean
    get() = Looper.getMainLooper().isCurrentThread

internal fun assertMainThread() {
    assert(isMainThread) { "只能在主线程中调用当前函数" }
}